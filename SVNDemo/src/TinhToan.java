public class TinhToan {
	public static int cong(int a,int b)
	{
		return a+b;
	}
	
	public static int tru(int a,int b)
	{
		return a-b;
	}
	
	public static int nhan(int a,int b)
	{
		return a*b;
	}
	
	public static int chia(int a,int b)
	{
		return a/b;
	}
	
	public static int div(int a,int b)
	{
		return a%b;
	}

}
